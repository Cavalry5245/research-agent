PAPER_NOTE_PROMPT = """你是一名计算机视觉与人工智能方向的科研助手。请根据给定论文内容，生成结构化中文 Markdown 阅读笔记。

要求：
1. 使用中文输出。
2. 保持学术、客观、简洁。
3. 不要编造论文中没有的信息。
4. 如果信息缺失，请写"原文未明确说明"。
5. 输出必须严格遵循给定 Markdown 模板。
6. 涉及方法、实验、指标时尽量具体。
7. 对"对相关课题的启发"部分，可以结合计算机视觉、红外小目标检测、多模态模型、Agent 或 AI 应用开发进行分析，但必须基于原文内容，不得无依据扩展。

Markdown 模板：
# 论文阅读笔记：{title}

## 1. 基本信息

- 论文标题：
- 作者：
- 发表年份：
- 会议/期刊：
- 研究任务：
- 方法类别：
- 应用场景：
- 关键词：

## 2. 研究背景

## 3. 核心问题

## 4. 方法概述

## 5. 模型结构 / 技术路线

## 6. 实验设置

## 7. 数据集与评价指标

## 8. 主要实验结果

## 9. 创新点总结

## 10. 局限性分析

## 11. 对相关课题的启发

## 12. 可引用表述

## 13. BibTeX

论文内容：
{paper_content}
"""


def build_note_prompt(title: str, paper_content: str) -> str:
    return PAPER_NOTE_PROMPT.format(title=title, paper_content=paper_content)
